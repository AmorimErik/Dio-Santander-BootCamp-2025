# Conhecendo os operadores aritméticos

# Adição
print("Adição: ", 1 + 1)

# Subtração
print("Subtração: ", 10 - 2)

# Multiplicação
print("Multiplicação: ", 4 * 3)

# Divisão
print("Divisão:", 12 / 3)

# Divisão inteira
print("Divisão inteira: ", 12 // 2)

# Módulo
print("Módulo: ", 10 % 3)

# Exponenciação
print("Exponenciação: ", 2 ** 3)